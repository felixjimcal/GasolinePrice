package Models;

public class ProductosPetroliferos {

    String IDProducto, NombreProducto, NombreProductoAbreviatura;

    public String getIDProducto() {
        return IDProducto;
    }

    public void setIDProducto(String IDProducto) {
        this.IDProducto = IDProducto;
    }

    public String getNombreProducto() {
        return NombreProducto;
    }

    public void setNombreProducto(String nombreProducto) {
        NombreProducto = nombreProducto;
    }

    public String getNombreProductoAbreviatura() {
        return NombreProductoAbreviatura;
    }

    public void setNombreProductoAbreviatura(String nombreProductoAbreviatura) {
        NombreProductoAbreviatura = nombreProductoAbreviatura;
    }
}
